using namespace std;


void makeOutput(float f) {
    cout << "Float: " << f << endl;
}

void makeOutput(double d) {
    cout << "Double: " << d << endl;
}

void makeOutput(int i) {
    cout << "Int: " << i << endl;
}

void makeOutput(long l) {
    cout << "Long: " << l << endl;
}

void makeOutput(int i, float f) {
    cout << "Int and float: " << i << ", " << f << endl;
}